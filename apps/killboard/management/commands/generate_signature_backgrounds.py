"""Combat Signatures — deterministic procedural background generator (WS-2).

Draws the whole committed background library (``static/killboard/sigbg/<key>/*.png`` plus
``manifest.json``) from fixed integer seeds. Every design is wholly original abstract art —
no CCP artwork, logos, text or insignia — combining starfields, nebula banks, warp streaks,
tracer fire, drone swarms, tactical grids, wormhole rings, cyno flares and engine glows,
anchored on the app's Tailwind palette (see ``apps.killboard.signature_assets``).

Determinism is the contract that lets the test suite verify the committed bytes: every design
is drawn only from ``random.Random(seed)`` (never the global RNG, never the clock), rendered at
2× and LANCZOS-downscaled for clean edges, then quantized to a 256-colour adaptive palette. Run
inside the container (matching Pillow/zlib) so regenerated bytes are identical.

    manage.py generate_signature_backgrounds            # (re)build the whole library + manifest
    manage.py generate_signature_backgrounds --only KEY # rebuild one design in place
    manage.py generate_signature_backgrounds --check    # regenerate to a temp dir, diff checksums

``--check`` never touches the committed tree; it exits non-zero on any drift or contrast failure.
"""
