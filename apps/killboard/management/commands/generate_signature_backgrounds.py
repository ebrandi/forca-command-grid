_REF_AREA = 1200.0 * 300.0  # the 2× standard preset — the density reference


def _area(w: int, h: int) -> float:
    """A density multiplier so element counts stay visually even across preset aspect ratios."""
    return (w * h) / _REF_AREA


def _mul(color, k: float):
    """A colour scaled by ``k`` and clamped to 0..255 (for brightness/alpha modulation)."""
    return tuple(max(0, min(255, int(round(c * k)))) for c in color)


def _scale_layer(layer, k: float):
    return layer.point(lambda v: max(0, min(255, int(v * k))))


def _stack(base, *layers):
    """Additively composite glowing ``layers`` (drawn on black) over ``base``."""
    out = base
    for layer in layers:
        out = ImageChops.add(out, layer)
    return out


