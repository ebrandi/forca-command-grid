def _vgrad(size, top, bottom):
    w, h = size
    col = Image.new("RGB", (1, h))
    px = col.load()
    for y in range(h):
        t = y / max(1, h - 1)
        px[0, y] = tuple(int(top[i] + (bottom[i] - top[i]) * t) for i in range(3))
    return col.resize((w, h))


def _black(size):
    return Image.new("RGB", size, (0, 0, 0))


def starfield(rng, size, *, count, tint=(255, 255, 255), max_r=1.6, bright=(50, 255), flares=0):
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    w, h = size
    strong = []
    for _ in range(count):
        x, y = rng.uniform(0, w), rng.uniform(0, h)
        b = rng.uniform(*bright)
        roll = rng.random()
        rad = 0.6 if roll < 0.7 else (1.1 if roll < 0.93 else max_r)
        col = _mul(tint, b / 255.0)
        if rad <= 0.7:
            d.point((x, y), fill=col)
        else:
            d.ellipse((x - rad, y - rad, x + rad, y + rad), fill=col)
        if b > 215 and rad >= 1.1:
            strong.append((x, y, col))
    for (x, y, col) in strong[:flares]:
        fl = _mul(col, 0.5)
        length = rng.uniform(3, 6)
        d.line((x - length, y, x + length, y), fill=fl)
        d.line((x, y - length, x, y + length), fill=fl)
    return layer


def nebula(rng, size, *, hues, blobs, blur, gain=1.0, spread=1.0, region=None):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    rx0, ry0, rx1, ry1 = region or (0, 0, w, h)
    for _ in range(blobs):
        cx, cy = rng.uniform(rx0, rx1), rng.uniform(ry0, ry1)
        rw = rng.uniform(0.12, 0.42) * (rx1 - rx0) * spread
        rh = rng.uniform(0.12, 0.42) * (ry1 - ry0) * spread
        b = rng.uniform(0.25, 1.0)
        d.ellipse((cx - rw, cy - rh, cx + rw, cy + rh), fill=_mul(rng.choice(hues), b))
    layer = layer.filter(ImageFilter.GaussianBlur(blur))
    return _scale_layer(layer, gain) if gain != 1.0 else layer


def glow(size, center, radius, color, *, gain=1.0, blur=None):
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    cx, cy = center
    d.ellipse((cx - radius, cy - radius, cx + radius, cy + radius), fill=_mul(color, gain))
    return layer.filter(ImageFilter.GaussianBlur(blur if blur is not None else radius * 0.6))


def warp_streaks(rng, size, *, vanish, count, color, gain=1.0, min_len=0.2, max_len=0.9,
                 blur=1.2, spread=1.0, r0_range=(0.05, 0.35)):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    vx, vy = vanish
    reach = max(w, h)
    for _ in range(count):
        ang = rng.uniform(0, 2 * math.pi)
        r0 = rng.uniform(*r0_range) * reach
        r1 = r0 + rng.uniform(min_len, max_len) * reach
        ca, sa = math.cos(ang), math.sin(ang) * spread
        x0, y0 = vx + ca * r0, vy + sa * r0
        x1, y1 = vx + ca * r1, vy + sa * r1
        b = rng.uniform(0.3, 1.0)
        width = rng.choice([1, 1, 1, 2])
        segs = 5
        for s in range(segs):
            t0, t1 = s / segs, (s + 1) / segs
            d.line((x0 + (x1 - x0) * t0, y0 + (y1 - y0) * t0,
                    x0 + (x1 - x0) * t1, y0 + (y1 - y0) * t1),
                   fill=_mul(color, gain * b * (1 - t0 * 0.8)), width=width)
    return layer.filter(ImageFilter.GaussianBlur(blur))


def tracer_dashes(rng, size, *, count, color, angle_deg, gain=1.0, region=None,
                  length=(0.04, 0.12), blur=0.8):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    rx0, ry0, rx1, ry1 = region or (0, 0, w, h)
    reach = max(w, h)
    for _ in range(count):
        x, y = rng.uniform(rx0, rx1), rng.uniform(ry0, ry1)
        length_px = rng.uniform(*length) * reach
        aa = math.radians(angle_deg + rng.uniform(-8, 8))
        ca, sa = math.cos(aa), math.sin(aa)
        b = rng.uniform(0.5, 1.0)
        width = rng.choice([1, 2, 2, 3])
        ex, ey = x + ca * length_px, y + sa * length_px
        d.line((x, y, ex, ey), fill=_mul(color, gain * b), width=width)
        d.ellipse((ex - 1.6, ey - 1.6, ex + 1.6, ey + 1.6), fill=_mul(color, min(1.0, gain * b * 1.3)))
    return layer.filter(ImageFilter.GaussianBlur(blur))


def smoke_trail(size, *, p0, p1, p2, color, gain=1.0, width0=6, blur=2.5):
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    steps = 60
    for i in range(steps + 1):
        t = i / steps
        x = (1 - t) ** 2 * p0[0] + 2 * (1 - t) * t * p1[0] + t ** 2 * p2[0]
        y = (1 - t) ** 2 * p0[1] + 2 * (1 - t) * t * p1[1] + t ** 2 * p2[1]
        r = width0 * (1 - t) * 0.9 + 1
        d.ellipse((x - r, y - r, x + r, y + r), fill=_mul(color, gain * (0.25 + 0.75 * (1 - t))))
    return layer.filter(ImageFilter.GaussianBlur(blur))


def drone_swarm(rng, size, *, count, color, region, gain=1.0):
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    rx0, ry0, rx1, ry1 = region
    for _ in range(count):
        x, y = rng.uniform(rx0, rx1), rng.uniform(ry0, ry1)
        s = rng.uniform(1.5, 4.0)
        ang = rng.uniform(0, 2 * math.pi)
        pts = [
            (x + math.cos(ang) * s, y + math.sin(ang) * s),
            (x + math.cos(ang + 2.5) * s * 0.7, y + math.sin(ang + 2.5) * s * 0.7),
            (x + math.cos(ang - 2.5) * s * 0.7, y + math.sin(ang - 2.5) * s * 0.7),
        ]
        b = rng.uniform(0.3, 0.8)
        d.polygon(pts, fill=_mul(color, gain * b * 0.5))
        d.point((x, y), fill=_mul(color, min(1.0, gain * b * 1.4)))
    return layer.filter(ImageFilter.GaussianBlur(0.6))


def hex_grid(size, *, color, radius=26, gain=1.0, blur=0.5):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    col = _mul(color, gain)
    dx = radius * math.sqrt(3)
    dy = radius * 1.5
    y = -radius
    row = 0
    while y < h + radius:
        xoff = 0 if row % 2 == 0 else dx / 2
        x = -radius
        while x < w + dx:
            cx, cy = x + xoff, y
            pts = [(cx + radius * math.cos(math.radians(60 * k + 30)),
                    cy + radius * math.sin(math.radians(60 * k + 30))) for k in range(6)]
            d.polygon(pts, outline=col)
            x += dx
        y += dy
        row += 1
    return layer.filter(ImageFilter.GaussianBlur(blur))


def perspective_grid(size, *, color, horizon=0.55, gain=1.0, cols=16, blur=0.6):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    col = _mul(color, gain)
    hy = h * horizon
    vpx = w * 0.5
    for i in range(cols + 1):
        d.line((w * i / cols, h, vpx, hy), fill=col)
    rows = 10
    for j in range(1, rows + 1):
        t = j / rows
        y = hy + (h - hy) * (t * t)
        d.line((0, y, w, y), fill=_mul(color, gain * (0.3 + 0.7 * t)))
    return layer.filter(ImageFilter.GaussianBlur(blur))


def rings(rng, size, *, center, color, count, gain=1.0, distort=0.0, blur=0.8, rmax=0.9):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    cx, cy = center
    base = rmax * max(w, h) * 0.5
    for i in range(1, count + 1):
        r = base * i / count
        rx = r * (1 + distort * rng.uniform(-1, 1)) if distort else r
        ry = r * (1 + distort * rng.uniform(-1, 1)) if distort else r
        b = gain * (0.35 + 0.65 * (1 - i / count))
        d.ellipse((cx - rx, cy - ry, cx + rx, cy + ry), outline=_mul(color, b))
    return layer.filter(ImageFilter.GaussianBlur(blur))


def scanlines(size, *, color, gap=6, gain=0.5):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    col = _mul(color, gain)
    y = 0
    while y < h:
        d.line((0, y, w, y), fill=col)
        y += gap
    return layer


def radar_sweep(size, *, center, color, angle_deg, width_deg=44, gain=0.5, radius=0.9, blur=6):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    cx, cy = center
    r = radius * max(w, h) * 0.5
    d.pieslice((cx - r, cy - r, cx + r, cy + r), angle_deg, angle_deg + width_deg, fill=_mul(color, gain))
    return layer.filter(ImageFilter.GaussianBlur(blur))


def corner_brackets(size, *, color, gain=0.7, inset=0.05, length=0.08, width=2):
    w, h = size
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    col = _mul(color, gain)
    ix, iy = w * inset, h * inset
    lx, ly = w * length, h * length
    for (x, y, sx, sy) in [(ix, iy, 1, 1), (w - ix, iy, -1, 1), (ix, h - iy, 1, -1), (w - ix, h - iy, -1, -1)]:
        d.line((x, y, x + lx * sx, y), fill=col, width=width)
        d.line((x, y, x, y + ly * sy), fill=col, width=width)
    return layer


def cyno_flare(size, *, center, color, gain=1.0, core=10, streak=0.8):
    w, h = size
    cx, cy = center
    layer = _black(size)
    d = ImageDraw.Draw(layer)
    d.ellipse((cx - core, cy - core, cx + core, cy + core), fill=_mul(color, gain))
    reach = streak * max(w, h) * 0.5
    for (dx, dy, wd) in [(1, 0, 2), (0, 1, 2), (0.7, 0.7, 1), (0.7, -0.7, 1)]:
        d.line((cx - dx * reach, cy - dy * reach, cx + dx * reach, cy + dy * reach),
               fill=_mul(color, gain * 0.5), width=wd)
    layer = layer.filter(ImageFilter.GaussianBlur(2.0))
    halo = _black(size)
    ImageDraw.Draw(halo).ellipse((cx - core * 3, cy - core * 3, cx + core * 3, cy + core * 3),
                                 fill=_mul(color, gain * 0.6))
    return ImageChops.add(layer, halo.filter(ImageFilter.GaussianBlur(core * 2)))


