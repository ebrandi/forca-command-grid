def d_fleet_formation(rng, w, h):
    base = _vgrad((w, h), SPACE, _mul(GOLDDUST, 0.08))
    stars = starfield(rng, (w, h), count=int(120 * _area(w, h)), bright=(40, 200))
    out = _stack(base, stars)
    engines = _black((w, h))
    de = ImageDraw.Draw(engines)
    dsil = ImageDraw.Draw(out)
    vx, vy = w * 0.92, h * 0.28
    ranks = 5
    for r in range(ranks):
        t = r / (ranks - 1)
        scale = 0.35 + 0.65 * t
        count = r + 1
        for k in range(count):
            spread = (k - (count - 1) / 2)
            cx = vx - t * w * 0.55 + spread * 0.05 * w * t
            cy = vy + spread * 0.14 * h * (0.4 + t)
            sl = 0.045 * w * scale
            sh = sl * 0.5
            dsil.polygon([(cx, cy), (cx + sl, cy - sh), (cx + sl * 1.15, cy), (cx + sl, cy + sh)],
                         fill=_mul(SPACE, 0.6))
            de.ellipse((cx - 2.5, cy - 2, cx + 2.5, cy + 2), fill=_mul(GOLD, 0.9 * (0.4 + t)))
    out = ImageChops.add(out, engines.filter(ImageFilter.GaussianBlur(2.2)))
    return out


