def _render_preset(design: dict, size: tuple[int, int]):
    """Render one design at ``size`` (2× internally), scrimmed, downscaled — RGB, pre-quantize."""
    import random

    w, h = size
    rng = random.Random(design["seed"])  # noqa: S311 — procedural art, not cryptography
    art = design["fn"](rng, w * SUPERSAMPLE, h * SUPERSAMPLE)
    scrimmed = apply_safe_scrim(art)
    return scrimmed.resize((w, h), Image.LANCZOS)


def _encode_png(img) -> bytes:
    quant = img.convert("RGB").quantize(colors=256, method=Image.Quantize.MEDIANCUT,
                                        dither=Image.Dither.FLOYDSTEINBERG)
    buf = io.BytesIO()
    quant.save(buf, format="PNG", optimize=True)
    return buf.getvalue()


def _build_design_pngs(design: dict) -> dict[str, bytes]:
    """All committed PNG bytes for a design, keyed by file name (presets + thumb)."""
    out: dict[str, bytes] = {}
    std_master = None
    for preset, size in PRESETS.items():
        master = _render_preset(design, size)
        if preset == "standard":
            std_master = master
        out[preset] = _encode_png(master)
    out[THUMB_KEY] = _encode_png(std_master.resize(preset_size(THUMB_KEY), Image.LANCZOS))
    return out


def _sha256(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def _combined_checksum(files: dict[str, str]) -> str:
    """A single per-design checksum folding in every file's digest (order-stable)."""
    joined = "".join(f"{k}:{files[k]}" for k in sorted(files))
    return hashlib.sha256(joined.encode("utf-8")).hexdigest()


def _manifest_entry(design: dict, pngs: dict[str, bytes]) -> dict:
    files = {}
    per_sha = {}
    for name in FILE_KEYS:
        sha = _sha256(pngs[name])
        files[name] = {"path": canonical_rel(design["key"], name), "sha256": sha}
        per_sha[name] = sha
    return {
        "key": design["key"],
        "name": design["name"],
        "category": design["category"],
        "seed": design["seed"],
        "display_order": design["display_order"],
        "version": MODEL_VERSION,
        "checksum": _combined_checksum(per_sha),
        "files": files,
        "provenance": {
            "origin": "procedural-original",
            "source": "generate_signature_backgrounds",
            "license": "MIT (project)",
            "notes": design["notes"],
        },
    }


class Command(BaseCommand):
    help = "Generate the Combat Signatures background library (deterministic procedural art)."

    def add_arguments(self, parser) -> None:
        parser.add_argument("--only", metavar="KEY", default=None,
                            help="Regenerate only this design key (in place).")
        parser.add_argument("--check", action="store_true",
                            help="Regenerate into a temp dir and diff checksums; exit 1 on drift.")
        parser.add_argument("--out", metavar="DIR", default=None,
                            help="Write into DIR instead of the committed static tree (testing).")

    def handle(self, *args, **options) -> None:
        only = options["only"]
        designs = [_design_by_key(only)] if only else DESIGNS
        if options["check"]:
            self._run_check(designs)
            return
        out_root = Path(options["out"]) if options["out"] else sigbg_dir()
        self._write(designs, out_root, only=only)

    # -- write mode ---------------------------------------------------------- #
    def _write(self, designs, out_root: Path, *, only) -> None:
        out_root.mkdir(parents=True, exist_ok=True)
        # Preserve untouched entries when regenerating a single design.
        existing = {}
        mpath = out_root / "manifest.json"
        if mpath.exists():
            existing = {bg["key"]: bg for bg in load_manifest(mpath).get("backgrounds", [])}
        entries = dict(existing) if only else {}
        for design in designs:
            dir_path = out_root / design["key"]
            dir_path.mkdir(parents=True, exist_ok=True)
            pngs = _build_design_pngs(design)
            for name, data in pngs.items():
                (dir_path / f"{name}.png").write_bytes(data)
                img = Image.open(io.BytesIO(data)).convert("RGB")
                if not text_zone_ok(img):
                    raise CommandError(f"{design['key']} {name}: fails text_zone_ok (safe area too bright)")
            entries[design["key"]] = _manifest_entry(design, pngs)
            self.stdout.write(f"  {design['key']}: {len(pngs)} files")
        manifest = {
            "generator_version": GENERATOR_VERSION,
            "generated": GENERATED_DATE,
            "backgrounds": sorted(entries.values(), key=lambda e: (e["display_order"], e["key"])),
        }
        mpath.write_text(_dump_manifest(manifest), encoding="utf-8")
        self.stdout.write(self.style.SUCCESS(
            f"Wrote {len(designs)} design(s); manifest lists {len(manifest['backgrounds'])}."
        ))

    # -- check mode ---------------------------------------------------------- #
    def _run_check(self, designs) -> None:
        committed = {bg["key"]: bg for bg in load_manifest().get("backgrounds", [])}
        drift = []
        with tempfile.TemporaryDirectory() as tmp:
            tmp_root = Path(tmp)
            for design in designs:
                key = design["key"]
                want = committed.get(key)
                pngs = _build_design_pngs(design)
                dir_path = tmp_root / key
                dir_path.mkdir(parents=True, exist_ok=True)
                for name in FILE_KEYS:
                    fpath = dir_path / f"{name}.png"
                    fpath.write_bytes(pngs[name])
                    got = _sha256(fpath.read_bytes())
                    expect = (want or {}).get("files", {}).get(name, {}).get("sha256")
                    if want is None:
                        drift.append(f"{key}: not in committed manifest")
                        break
                    if got != expect:
                        drift.append(f"{key}/{name}.png: checksum drift")
                    if not text_zone_ok(Image.open(fpath).convert("RGB")):
                        drift.append(f"{key}/{name}.png: fails text_zone_ok")
        if drift:
            for line in drift:
                self.stderr.write(self.style.ERROR(line))
            raise CommandError(f"{len(drift)} background(s) drifted from the committed library.")
        self.stdout.write(self.style.SUCCESS(f"OK — {len(designs)} design(s) match the committed library."))


def _dump_manifest(manifest: dict) -> str:
    import json

    return json.dumps(manifest, indent=2, ensure_ascii=False) + "\n"
