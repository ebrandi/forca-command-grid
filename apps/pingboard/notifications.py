"""The notification-event registry + governance policy.

Every place in the app that *pushes* a message out (a Discord/Slack/Telegram/WhatsApp
broadcast, an EVE-mail digest, an in-app alert generated by a background job) is
catalogued here as a **notification event**. The registry is the single source of
truth for:

* **documentation** — one authoritative list of what generates notifications
  (mirrored in ``handbooks/reference/background-jobs.md``);
* **governance** — each event carries an *audience* (who it is for) and a
  *sensitive* flag (is it leadership-oriented?). Leadership-oriented events must
  never reach a mass chat channel; they resolve to a leadership *classification*
  that the Pingboard sink (:func:`apps.pingboard.services.broadcast_text` and the
  :class:`~apps.pingboard.dispatch.AlertDispatcher`) refuses to post to a channel
  whose ``max_classification`` ceiling is below it;
* **configuration** — leadership tunes each event (enable/disable, change the
  audience, adjust the severity floor) and sets *who* counts as corp leadership,
  from the ``/ops/admin/notifications/`` console page, with no deploy.

Policy overrides live in the ``pingboard.notifications`` config document (the exact
``apps.pingboard.config`` versioned-JSON pattern); this module only reads them.
The registry defaults encode today's shipped behaviour, so an install with no
overrides behaves exactly as the catalogue describes.
"""
from __future__ import annotations

from dataclasses import dataclass

from django.utils.translation import gettext_lazy as _

from . import config

# --- audience → classification -------------------------------------------------
# The audience an event is *for* determines the lowest channel classification that
# may carry it. ``corp``/``public``/``member`` are corp-internal (any channel);
# ``officer`` is high-command; ``director``/``admin`` are director-eyes-only; a
# targeted ``user``/``users`` DM must never fan out to a shared channel, so it is
# treated as director-eyes-only for broadcast purposes (per-recipient legs — in-app,
# EVE-mail, verified DM handles — still deliver to exactly the addressed pilots).
_AUDIENCE_CLASSIFICATION: dict[str, str | None] = {
    "corp": None,
    "public": None,
    "member": None,
    "officer": "high_command",
    "director": "director_eyes_only",
    "admin": "director_eyes_only",
    "user": "director_eyes_only",
    "users": "director_eyes_only",
}

# Audience values a director may pick for an event on the console.
AUDIENCE_CHOICES = ("corp", "officer", "director")
_LEADERSHIP_ROLE_CHOICES = ("member", "officer", "director", "admin")


def classification_for_audience(audience: str | None) -> str | None:
    """The classification tier an audience string implies (``None`` = corp-internal)."""
    return _AUDIENCE_CLASSIFICATION.get(audience or "corp", None)


# --- the catalogue -----------------------------------------------------------
@dataclass(frozen=True)
class Event:
    """One catalogued notification event.

    ``sensitive`` marks leadership-oriented traffic; its default audience is a
    restricted role, so out of the box it never reaches a mass chat channel. Leadership
    can widen the audience to ``corp`` (opting into a broadcast) or designate a
    leadership channel (raise a provider's ``max_classification`` ceiling) — both via
    the console. ``broadcast`` records whether the event fans out to chat channels at
    all (vs. purely in-app / EVE-mail). ``min_severity`` applies only to the
    recommendation digest.
    """

    key: str
    label: str
    description: str
    group: str
    source_service: str
    audience: str = "corp"
    sensitive: bool = False
    broadcast: bool = True
    min_severity: int | None = None
    triggers: str = ""  # human note: what/when fires it (for the docs + UI)

    def defaults(self) -> dict:
        d = {"enabled": True, "audience": self.audience}
        if self.min_severity is not None:
            d["min_severity"] = self.min_severity
        return d


