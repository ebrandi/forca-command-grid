_BY_KEY: dict[str, Event] = {e.key: e for e in REGISTRY}


def event(key: str) -> Event | None:
    return _BY_KEY.get(key)


# --- effective policy --------------------------------------------------------
def _policy() -> dict:
    return config.get("notifications")


def resolve(key: str) -> dict:
    """The effective policy for an event: registry defaults ⊕ stored overrides.

    Returns ``{key, label, enabled, audience, sensitive, broadcast, classification,
    min_severity}``. ``classification`` is derived from the (possibly overridden)
    audience — it is what the broadcast sink checks against each channel's ceiling, so
    changing an event's audience on the console immediately changes whether it may reach
    a mass channel. An unknown key resolves to an enabled, corp-audience passthrough so a
    new caller is never silently muted before its catalogue entry lands.
    """
    ev = _BY_KEY.get(key)
    overrides = (_policy().get("events") or {}).get(key) or {}
    if ev is None:
        audience = overrides.get("audience", "corp")
        return {
            "key": key, "label": key, "enabled": bool(overrides.get("enabled", True)),
            "audience": audience, "sensitive": False, "broadcast": True,
            "classification": classification_for_audience(audience),
            "min_severity": overrides.get("min_severity"),
        }
    audience = overrides.get("audience", ev.audience)
    if audience not in _AUDIENCE_CLASSIFICATION:
        audience = ev.audience
    return {
        "key": ev.key, "label": ev.label, "enabled": bool(overrides.get("enabled", True)),
        "audience": audience, "sensitive": ev.sensitive, "broadcast": ev.broadcast,
        "classification": classification_for_audience(audience),
        "min_severity": int(overrides.get("min_severity", ev.min_severity or 0)) or ev.min_severity,
    }


def is_enabled(key: str) -> bool:
    return resolve(key)["enabled"]


def broadcast_classification(key: str) -> str | None:
    """The classification a broadcast for this event must carry (``None`` = corp-internal).

    Passed straight to :func:`apps.pingboard.services.broadcast_text`, so a leadership
    event (default audience ``officer`` → ``high_command``) is dropped by every chat
    channel that has not been explicitly designated a leadership channel.
    """
    return resolve(key)["classification"]


# --- leadership distribution -------------------------------------------------
def leadership_role() -> str:
    role = _policy().get("leadership_role", "officer")
    return role if role in _LEADERSHIP_ROLE_CHOICES else "officer"


def leadership_user_ids() -> list[int]:
    ids = _policy().get("leadership_user_ids") or []
    return [int(i) for i in ids if isinstance(i, int) or (isinstance(i, str) and str(i).isdigit())]


def leadership_audience() -> dict:
    """The audience spec that addresses corp leadership for a restricted alert.

    Explicit named recipients win when set; otherwise everyone at or above the
    configured leadership role. Used as the in-app / EVE-mail distribution for
    leadership-restricted alerts and offered to callers that want to reach leadership
    directly rather than via a channel.
    """
    ids = leadership_user_ids()
    if ids:
        return {"kind": "users", "ids": ids}
    return {"kind": leadership_role()}
